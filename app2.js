function parseTextToJSON(text) {
    const lines = text.split('\n').map(l => l.trim()).filter(l => l.length > 0);
    const themes = [];
    let currentTheme = null;
    let currentQuestion = null;

    lines.forEach(line => {
        if (line.startsWith('Theme ')) {
            if (currentQuestion) currentTheme.questions.push(currentQuestion);
            if (currentTheme) themes.push(currentTheme);
            currentTheme = {
                title: line,
                questions: []
            };
            currentQuestion = null;
        } else if (/^\d+\./.test(line)) {
            if (currentQuestion) currentTheme.questions.push(currentQuestion);
            currentQuestion = {
                text: line,
                options: []
            };
        } else if (/^[A-E]\)/.test(line)) {
            if (currentQuestion) {
                currentQuestion.options.push({ text: line, prefix: line.substring(0, 1) });
            }
        } else {
            // Continuation of previous line
            if (currentQuestion && currentQuestion.options.length === 0) {
                currentQuestion.text += " " + line;
            } else if (currentTheme && !currentQuestion) {
                currentTheme.title += " " + line;
            }
        }
    });

    if (currentQuestion) currentTheme.questions.push(currentQuestion);
    if (currentTheme) themes.push(currentTheme);
    
    // Assign answers
    themes.forEach((theme, themeIdx) => {
        theme.questions.forEach((q, qIdx) => {
            q.correctIndices = [];
            if (CORRECT_ANSWERS[themeIdx] && CORRECT_ANSWERS[themeIdx][qIdx]) {
                const correctLetters = CORRECT_ANSWERS[themeIdx][qIdx];
                q.options.forEach((opt, optIdx) => {
                    if (correctLetters.includes(opt.prefix)) {
                        q.correctIndices.push(optIdx);
                    }
                });
            }
        });
    });

    return themes;
}

